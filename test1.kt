// functions
// we can create functions with fun key word followed by function name followed by open and closed parentheses
// with no parameters
fun myFunction() {
    println("hello world")
}

// with parameters 
// we define paramters like name of parameter : type of parameter
fun myFunction1(name : String) {
    println("name is $name")
}
fun main() {
    myFunction()
    myFunction1("naresh")
}